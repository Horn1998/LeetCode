    def uniquePathsWithObstacles(self, obstacleGrid):
        def tracktrace(x, y):
            if x >= len(obstacleGrid) or y >= len(obstacleGrid[0]) or obstacleGrid[x][y] == 1:
                return
            elif x == len(obstacleGrid) - 1 and y == len(obstacleGrid[0]) - 1:
                self.count += 1
                return
            else:
                tracktrace(x + 1, y)
                tracktrace(x, y + 1)
        tracktrace(0, 0)
        print(self.count)
        return self.count
